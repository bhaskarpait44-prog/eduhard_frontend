// ── PDF 4 — Low Stock Alert Report ─────────────────────────────────────

export function generateLowStockPdf(data) {
  const { school, items = [], generated_by, generated_at } = data

  const doc = new jsPDF({ unit: 'mm', format: 'a4' })

  const subtitle = `Generated: ${formatDate(generated_at)}  |  By: ${generated_by || 'Administrator'}`
  let y = drawHeader(doc, school, 'LOW STOCK ALERT REPORT', subtitle)

  const categoriesCount = [...new Set(items.map(i => i.category))].length

  y = drawSummaryBox(doc, y, [
    { label: 'Items Below Limit',    value: items.length },
    { label: 'Categories Affected',  value: categoriesCount },
    { label: 'Immediate Action Req', value: items.length > 0 ? 'YES' : 'NO' },
  ])

  autoTable(doc, {
    startY: y,
    head: [['#', 'Item Name', 'Category', 'Unit', 'Current Stock', 'Min Level', 'Shortfall']],
    body: items.map((item, idx) => [
      idx + 1,
      item.name,
      item.category,
      item.unit,
      formatQty(item.quantity),
      formatQty(item.reorder_level),
      formatQty(parseFloat(item.reorder_level) - parseFloat(item.quantity)),
    ]),
    margin: { left: MARGIN, right: MARGIN },
    headStyles: { fillColor: [200, 40, 40], textColor: 255, fontStyle: 'bold', fontSize: 8 },
    bodyStyles: { fontSize: 8, textColor: [180, 30, 30] },
    columnStyles: {
      0: { cellWidth: 10, halign: 'center' },
      1: { cellWidth: 55 },
      2: { cellWidth: 30 },
      3: { cellWidth: 20, halign: 'center' },
      4: { cellWidth: 22, halign: 'right' },
      5: { cellWidth: 22, halign: 'right' },
      6: { cellWidth: 22, halign: 'right' },
    },
    alternateRowStyles: { fillColor: [255, 248, 248] },
  })

  // Footer note
  const footerNoteY = doc.lastAutoTable.finalY + 10
  doc.setFontSize(8)
  doc.setFont('helvetica', 'italic')
  doc.setTextColor(120, 120, 120)
  doc.text('Note: Shortfall indicates the minimum quantity required to reach the reorder level.', MARGIN, footerNoteY)

  drawFooter(doc, generated_by, generated_at)
  doc.save(`Low_Stock_Alert_${new Date().toISOString().slice(0,10)}.pdf`)
}
